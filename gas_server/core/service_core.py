def _remove_stream_event_fields(value: Any) -> Any:
    stream_event_keys = {"events", "stream_events", "thinking_events"}
    if isinstance(value, dict):
        return {
            key: _remove_stream_event_fields(item)
            for key, item in value.items()
            if key not in stream_event_keys
        }
    if isinstance(value, list):
        return [_remove_stream_event_fields(item) for item in value]
    return value


def _build_task_payload(
    *,
    task_id: str,
    agent_id: str,
    agent_name: str | None,
    agent_version: str | None,
    state: str,
    query: str,
    requested_skill: str | None,
    result: Dict[str, Any] | None,
    error_message: str | None,
    agent_id_for_artifacts: str,
    output_delivery: str,
    public_base_url: str | None = None,
) -> Dict[str, Any]:
    transformed_result = (
        _transform_output_files(
            result,
            agent_id=agent_id_for_artifacts,
            output_delivery=output_delivery,
            public_base_url=public_base_url,
        )
        if result is not None
        else None
    )
    _rewrite_html_file_references(transformed_result)
    normalized_result = _strip_dataset_paths(transformed_result) if transformed_result is not None else None

    completed_dt = datetime.now(timezone.utc)
    duration_seconds = _parse_duration_seconds(normalized_result.get("duration") if isinstance(normalized_result, dict) else None)
    created_dt = completed_dt - timedelta(seconds=duration_seconds) if duration_seconds and _is_terminal_state(state) else completed_dt
    created_at = created_dt.isoformat()
    completed_at = completed_dt.isoformat() if _is_terminal_state(state) else None
    task_payload = _normalize_agent_result(
        task_id=task_id,
        agent_id=agent_id,
        agent_name=agent_name,
        agent_version=agent_version,
        state=state,
        query=query,
        requested_skill=requested_skill,
        result=normalized_result,
        error_message=error_message,
        output_delivery=output_delivery,
        created_at=created_at,
        completed_at=completed_at,
    )
    return _remove_stream_event_fields(task_payload)


