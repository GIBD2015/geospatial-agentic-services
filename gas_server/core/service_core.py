def handle_stream_debug_events() -> Response:
    limit_raw = request.args.get("limit", "200")
    try:
        limit = max(1, min(int(limit_raw), STREAM_DEBUG_MAX_EVENTS))
    except ValueError:
        limit = 200
    stream_debug_id = request.args.get("stream_debug_id")
    events = STREAM_DEBUG_LOGS
    if stream_debug_id:
        events = [event for event in events if event.get("stream_debug_id") == stream_debug_id]
    return _json_response(
        {
            "count": len(events[-limit:]),
            "events": events[-limit:],
        },
        HTTPStatus.OK,
    )


