def _is_local_file_reference(value: str) -> bool:
    if not isinstance(value, str):
        return False

    cleaned = value.strip()
    if not cleaned:
        return False

    # Reject obvious non-path payloads before touching the filesystem.
    if len(cleaned) > 512:
        return False
    if cleaned.startswith(("{", "[")):
        return False
    if "://" in cleaned:
        return False
    if "\n" in cleaned or "\r" in cleaned:
        return False
    if "\x00" in cleaned:
        return False
    if not any(token in cleaned for token in (os.sep, "/", "\\", ".")):
        return False

    try:
        path = Path(cleaned).expanduser()
    except (TypeError, ValueError, OSError):
        return False

    try:
        return path.is_file()
    except OSError:
        return False


def _mime_type_for_path(path: Path) -> str:
    suffix = path.suffix.lower()
    overrides = {
        ".geojson": "application/geo+json",
        ".gpkg": "application/geopackage+sqlite3",
        ".shp": "application/octet-stream",
        ".tif": "image/tiff",
        ".tiff": "image/tiff",
    }
    if suffix in overrides:
        return overrides[suffix]
    guessed, _ = mimetypes.guess_type(path.name)
    return guessed or "application/octet-stream"


def _public_base_url(explicit_base_url: str | None = None) -> str:
    if explicit_base_url:
        return explicit_base_url.rstrip("/")
    if not has_request_context():
        return DEFAULT_PUBLIC_BASE_URL
    forwarded = request.headers.get("X-Proxy-Base-Url")
    if forwarded:
        return forwarded.rstrip("/")
    return request.host_url.rstrip("/")


def _public_agent_id(spec: AgentSpec) -> str:
    return request.headers.get("X-Proxy-Agent-Id", spec.agent_id)


