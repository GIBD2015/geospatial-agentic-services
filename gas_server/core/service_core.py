def store_task(state: Dict[str, Any], task: Dict[str, Any]) -> Dict[str, Any]:
    task_id = task["task"]["id"]
    task_status = task["task"].get("status")
    lock = state.get("LOCK")
    if lock is None:
        state["TASKS"][task_id] = task
        if task_status in TERMINAL_STATUSES:
            state["CLOSED_TASKS"].add(task_id)
        return task

    with lock:
        state["TASKS"][task_id] = task
        if task_status in TERMINAL_STATUSES:
            state["CLOSED_TASKS"].add(task_id)
    return task


def _task_status(state: Dict[str, Any], task_id: str) -> str | None:
    lock = state.get("LOCK")
    if lock is None:
        task = state["TASKS"].get(task_id)
        return task.get("task", {}).get("status") if isinstance(task, dict) else None

    with lock:
        task = state["TASKS"].get(task_id)
        return task.get("task", {}).get("status") if isinstance(task, dict) else None


def _task_is_closed(state: Dict[str, Any], task_id: str) -> bool:
    lock = state.get("LOCK")
    if lock is None:
        return task_id in state["CLOSED_TASKS"]

    with lock:
        return task_id in state["CLOSED_TASKS"]


def _run_submitted_task_in_background(
    *,
    spec: AgentSpec,
    state: Dict[str, Any],
    task_id: str,
    query: str,
    dataset_paths: List[str],
    requested_skill: str | None,
    credentials: Dict[str, str | None],
    parameters: Dict[str, Any],
    output_delivery: str,
    public_agent_id: str,
    public_base_url: str,
    version: str,
) -> None:
    try:
        if _task_status(state, task_id) in TERMINAL_STATUSES or _task_is_closed(state, task_id):
            return

        running_task = _build_task_payload(
            task_id=task_id,
            agent_id=spec.agent_id,
            agent_name=None,
            agent_version=None,
            state="TASK_STATE_WORKING",
            query=query,
            requested_skill=requested_skill,
            result=None,
            error_message=None,
            agent_id_for_artifacts=public_agent_id,
            output_delivery=output_delivery,
            public_base_url=public_base_url,
        )
        store_task(state, running_task)

        agent = _build_agent_for_request(spec, parameters)
        agent_name = spec.get_name(agent)
        agent_version = spec.get_version(agent)
        _configure_agent_client_if_available(
            agent,
            agent_name=agent_name,
            credentials=credentials,
        )
        result = _call_agent_with_optional_progress(
            spec,
            agent,
            query,
            dataset_paths,
            parameters=parameters,
        )

        if _task_status(state, task_id) in TERMINAL_STATUSES or _task_is_closed(state, task_id):
            return

        completed_task = _build_task_payload(
            task_id=task_id,
            agent_id=spec.agent_id,
            agent_name=agent_name,
            agent_version=agent_version,
            state="TASK_STATE_COMPLETED",
            query=query,
            requested_skill=requested_skill,
            result=result,
            error_message=None,
            agent_id_for_artifacts=public_agent_id,
            output_delivery=output_delivery,
            public_base_url=public_base_url,
        )
        store_task(state, completed_task)
    except Exception as exc:
        if _task_status(state, task_id) in TERMINAL_STATUSES or _task_is_closed(state, task_id):
            return

        failed_task = _build_task_payload(
            task_id=task_id,
            agent_id=spec.agent_id,
            agent_name=None,
            agent_version=None,
            state="TASK_STATE_FAILED",
            query=query,
            requested_skill=requested_skill,
            result=None,
            error_message=str(exc),
            agent_id_for_artifacts=public_agent_id,
            output_delivery=output_delivery,
            public_base_url=public_base_url,
        )
        store_task(state, failed_task)


def submit_agent_task_from_payload(spec: AgentSpec, state: Dict[str, Any], payload: Dict[str, Any], version: str) -> Response:
    try:
        query, dataset_paths, metadata, _message = _extract_text_and_input_dataset_paths(payload)
    except ValueError as exc:
        return _gas_error(
            "INVALID_ARGUMENT",
            str(exc),
            status_code=HTTPStatus.BAD_REQUEST,
            version=version,
        )

    referenced_task_id = metadata.get("taskId") or metadata.get("task_id") or payload.get("taskId")
    if referenced_task_id and referenced_task_id in state["CLOSED_TASKS"]:
        return _gas_error(
            "TASK_CLOSED",
            f"Task '{referenced_task_id}' is already closed.",
            status_code=HTTPStatus.CONFLICT,
            version=version,
        )

    validation_error = spec.validate_inputs(dataset_paths)
    if validation_error:
        return _gas_error(
            "INVALID_ARGUMENT",
            validation_error,
            status_code=HTTPStatus.BAD_REQUEST,
            version=version,
        )

    credentials = _extract_request_credentials(metadata)
    if _requires_model_credentials(spec) and not _has_model_credentials(credentials):
        return _gas_error(
            "INVALID_ARGUMENT",
            "Request credentials must include either OPENAI_API_KEY or GIBD_API_KEY.",
            status_code=HTTPStatus.BAD_REQUEST,
            version=version,
        )

    try:
        output_delivery = _extract_output_delivery(metadata)
    except ValueError as exc:
        return _gas_error(
            "INVALID_ARGUMENT",
            str(exc),
            status_code=HTTPStatus.BAD_REQUEST,
            version=version,
        )

    task_id = str(uuid.uuid4())
    requested_skill = metadata.get("requested_skill")
    public_agent_id = _public_agent_id(spec)
    public_base_url = _public_base_url()
    submitted_task = _build_task_payload(
        task_id=task_id,
        agent_id=spec.agent_id,
        agent_name=None,
        agent_version=None,
        state="TASK_STATE_SUBMITTED",
        query=query,
        requested_skill=requested_skill,
        result=None,
        error_message=None,
        agent_id_for_artifacts=public_agent_id,
        output_delivery=output_delivery,
        public_base_url=public_base_url,
    )
    store_task(state, submitted_task)

    worker = threading.Thread(
        target=_run_submitted_task_in_background,
        kwargs={
            "spec": spec,
            "state": state,
            "task_id": task_id,
            "query": query,
            "dataset_paths": dataset_paths,
            "requested_skill": requested_skill,
            "credentials": credentials,
            "parameters": metadata,
            "output_delivery": output_delivery,
            "public_agent_id": public_agent_id,
            "public_base_url": public_base_url,
            "version": version,
        },
        daemon=True,
    )
    worker.start()

    response = _json_response(submitted_task, HTTPStatus.ACCEPTED, version)
    response.headers["Location"] = f"/tasks/{task_id}/status"
    return response


