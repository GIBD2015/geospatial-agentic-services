def _extract_task_mode(payload: Dict[str, Any]) -> str:
    task = payload.get("task") if isinstance(payload.get("task"), dict) else {}
    mode = task.get("mode") or payload.get("mode") or "sync"
    if not isinstance(mode, str):
        raise ValueError("task.mode must be one of 'sync', 'async', or 'stream'.")
    normalized = mode.strip().lower()
    if normalized not in {"sync", "async", "stream"}:
        raise ValueError("task.mode must be one of 'sync', 'async', or 'stream'.")
    return normalized


def _gas_execute_task_to_run_payload(payload: Dict[str, Any]) -> Dict[str, Any]:
    """Translate the GAS-native ExecuteTask body into the shared run payload."""
    task = payload.get("task") if isinstance(payload.get("task"), dict) else {}
    instructions = task.get("instructions") or payload.get("instructions")
    if not isinstance(instructions, str) or not instructions.strip():
        raise ValueError("task.instructions is required.")

    inputs = payload.get("inputs") if isinstance(payload.get("inputs"), dict) else {}
    outputs = payload.get("outputs") if isinstance(payload.get("outputs"), dict) else {}
    parameters = payload.get("parameters") if isinstance(payload.get("parameters"), dict) else {}
    credentials = payload.get("credentials") if isinstance(payload.get("credentials"), dict) else {}
    metadata = payload.get("metadata") if isinstance(payload.get("metadata"), dict) else {}

    params: Dict[str, Any] = {}
    params.update(metadata)
    params.update(parameters)
    params.update(credentials)

    if "input_datasets" in inputs:
        params["input_datasets"] = inputs.get("input_datasets")
    if "artifact_delivery" in outputs:
        params["output_delivery"] = outputs.get("artifact_delivery")
    for key, value in outputs.items():
        if key != "artifact_delivery":
            params.setdefault(key, value)

    params["mode"] = _extract_task_mode(payload)

    return {
        "query": instructions.strip(),
        "parameters": params,
        **params,
    }


