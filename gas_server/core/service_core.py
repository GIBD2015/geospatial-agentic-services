            event = _make_event(kind, **data)
            line = json.dumps(event) + "\n"
            _record_stream_debug(
                "yield_before",
                stream_debug_id=stream_debug_id,
                stream_event=kind,
                message=data.get("message"),
                line_bytes=len(line.encode("utf-8")),
            )
            yield line
            _record_stream_debug(
                "yield_after",
                stream_debug_id=stream_debug_id,
                stream_event=kind,
                message=data.get("message"),
            )
            time.sleep(0.1)

