def create_service_app(spec: AgentSpec) -> Flask:
    app = Flask(f"{spec.agent_id}_service")
    state = init_service_state()
    card = build_agent_card(spec)
    app.config["AGENT_SPEC"] = spec
    app.config["STATE"] = state
    app.config["CARD"] = card
    app.config["TASKS"] = state["TASKS"]
    app.config["CLOSED_TASKS"] = state["CLOSED_TASKS"]
    app.config["STREAM_AGENT_FROM_PAYLOAD"] = lambda payload, version=None: stream_agent_from_payload(spec, state, payload, version or "1.0.0")

    @app.route("/", methods=["GET"])
    def describe_agent() -> Response:
        return handle_describe_agent(spec)

    @app.route("/status", methods=["GET"])
    def agent_status() -> Response:
        return handle_agent_status(spec)

    @app.route("/tasks", methods=["POST"])
    def execute_task() -> Response:
        return handle_execute_task(spec, state)

    @app.route("/tasks/<task_id>/status", methods=["GET"])
    def get_task_status(task_id: str) -> Response:
        return handle_get_task_status(spec, state, task_id)

    @app.route("/tasks/<task_id>/result", methods=["GET"])
    def get_task_result(task_id: str) -> Response:
        return handle_get_task_result(spec, state, task_id)

    @app.route("/tasks/<task_id>/cancel", methods=["POST"])
    def cancel_task(task_id: str) -> Response:
        return handle_cancel_task(spec, state, task_id)

    @app.route("/data/<path:filename>", methods=["GET"])
    def get_data_file(filename: str) -> Response:
        return handle_get_data_file(spec, filename)

    if ENABLE_STREAM_DEBUG:
        @app.route("/debug/stream-events", methods=["GET"])
        def stream_debug_events() -> Response:
            return handle_stream_debug_events()

    return app




