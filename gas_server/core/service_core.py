def _iso_now() -> str:
    return datetime.now(timezone.utc).isoformat()


def _record_stream_debug(event: str, **data: Any) -> None:
    if not ENABLE_STREAM_DEBUG:
        return
    try:
        STREAM_DEBUG_LOGS.append(
            {
                "timestamp": _iso_now(),
                "event": event,
                **data,
            }
        )
        if len(STREAM_DEBUG_LOGS) > STREAM_DEBUG_MAX_EVENTS:
            del STREAM_DEBUG_LOGS[: len(STREAM_DEBUG_LOGS) - STREAM_DEBUG_MAX_EVENTS]
    except Exception:
        pass


def _json_response(payload: Dict[str, Any], status_code: int, version: str | None = None) -> Response:
    response = Response(
        json.dumps(payload, indent=2),
        status=status_code,
        mimetype="application/json",
    )
    return response


def _gas_error(
    code: str,
    message: str,
    *,
    status_code: int,
    version: str | None = None,
    data: Dict[str, Any] | None = None,
) -> Response:
    payload = {"error": {"code": code, "message": message}}
    if data is not None:
        payload["error"]["data"] = data
    return _json_response(payload, status_code, version)


def _normalize_input_dataset_strings(values: Any) -> List[str]:
    if values is None:
        return []
    if isinstance(values, str):
        values = [values]

    normalized: List[str] = []
    for value in values:
        if isinstance(value, str) and "\n" in value:
            normalized.extend(part.strip() for part in value.split("\n") if part.strip())
        elif value:
            normalized.append(str(value))
    return normalized


