def _sanitize_filename(name: str | None, fallback: str = "dataset.bin") -> str:
    candidate = Path((name or fallback).strip() or fallback).name
    cleaned = "".join(ch if ch.isalnum() or ch in {".", "_", "-"} else "_" for ch in candidate)
    cleaned = cleaned.strip("._") or fallback
    return cleaned


def _looks_like_url(value: str) -> bool:
    parsed = urlparse(value)
    return parsed.scheme in {"http", "https"} and bool(parsed.netloc)


def _download_dataset_url(url: str, upload_dir: Path) -> str:
    response = requests.get(url, timeout=1800)
    response.raise_for_status()

    parsed = urlparse(url)
    filename = _sanitize_filename(Path(parsed.path).name or "downloaded_dataset.bin")
    target = upload_dir / filename
    target.write_bytes(response.content)
    return str(target)


def _store_embedded_dataset(file_obj: Dict[str, Any], upload_dir: Path) -> str:
    filename = _sanitize_filename(
        file_obj.get("filename")
        or file_obj.get("name")
        or file_obj.get("original_filename")
        or file_obj.get("path")
        or "uploaded_dataset.bin"
    )

    target = upload_dir / filename
    kind = file_obj.get("kind")

    if kind == "downloadable_file" and file_obj.get("url"):
        return _download_dataset_url(str(file_obj["url"]), upload_dir)

    if isinstance(file_obj.get("data"), str) and file_obj.get("encoding", "base64") == "base64":
        target.write_bytes(base64.b64decode(file_obj["data"]))
        return str(target)

    raise ValueError("Uploaded dataset object must contain base64 data or a downloadable URL.")


def _parse_json_mapping(value: str | None) -> Dict[str, Any]:
    if not value:
        return {}
    try:
        parsed = json.loads(value)
    except ValueError:
        return {}
    return parsed if isinstance(parsed, dict) else {}


def _request_payload() -> Dict[str, Any] | None:
    payload = request.get_json(silent=True)
    if isinstance(payload, dict):
        return payload

    if not request.files and not request.form:
        return None

    params: Dict[str, Any] = {}
    params.update(_parse_json_mapping(request.form.get("metadata")))
    params.update(_parse_json_mapping(request.form.get("Params")))
    params.update(_parse_json_mapping(request.form.get("params")))

    query = (
        request.form.get("query")
        or request.form.get("text")
        or request.form.get("prompt")
        or request.form.get("task")
        or request.form.get("message")
        or ""
    )

    for key, value in request.form.items():
        if key in {"query", "text", "prompt", "task", "message", "metadata", "Params", "params"}:
            continue
        if key == INPUT_DATASETS_FIELD:
            params[key] = json.loads(value) if value.strip().startswith(("[", "{")) else value
        else:
            params.setdefault(key, value)

    uploaded_files: list[dict[str, Any]] = []
    for file_storage in request.files.values():
        filename = _sanitize_filename(file_storage.filename or "uploaded_dataset.bin")
        uploaded_files.append(
            {
                "filename": filename,
                "mime_type": file_storage.mimetype,
                "encoding": "base64",
                "data": base64.b64encode(file_storage.read()).decode("ascii"),
            }
        )
    if uploaded_files:
        existing = params.get(INPUT_DATASETS_FIELD)
        if isinstance(existing, list):
            params[INPUT_DATASETS_FIELD] = existing + uploaded_files
        elif existing:
            params[INPUT_DATASETS_FIELD] = [existing] + uploaded_files
        else:
            params[INPUT_DATASETS_FIELD] = uploaded_files

    return {
        "query": query,
        **params,
    }


def _normalize_input_datasets(value: Any) -> tuple[List[str], List[Dict[str, Any]]]:
    if value is None:
        return [], []
    items = value if isinstance(value, list) else [value]
    string_inputs: List[str] = []
    encoded_inputs: List[Dict[str, Any]] = []
    for item in items:
        if isinstance(item, str):
            string_inputs.extend(_normalize_input_dataset_strings(item))
        elif isinstance(item, dict):
            encoded_inputs.append(item)
    return string_inputs, encoded_inputs


def _materialize_dataset_inputs(
    input_dataset_strings: List[str],
    input_dataset_objects: List[Dict[str, Any]],
) -> List[str]:
    if not input_dataset_strings and not input_dataset_objects:
        return []

    upload_dir = _request_upload_dir()
    materialized: List[str] = []

    for input_dataset in input_dataset_strings:
        if _looks_like_url(input_dataset):
            materialized.append(_download_dataset_url(input_dataset, upload_dir))
        else:
            materialized.append(input_dataset)

    for file_obj in input_dataset_objects:
        materialized.append(_store_embedded_dataset(file_obj, upload_dir))

    return materialized


